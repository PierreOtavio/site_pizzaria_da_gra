    /**
     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show()
    {
        $user = Auth::user();
        $customer = $user->customer;

        return view('profile.show', compact('user', 'customer'));
    }

